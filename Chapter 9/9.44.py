def main():
    # Имеется 10 гирь: 100,200,300,500,1000,1200,1400,1500,2000,3000.
    # Сколькими способами можно составить вес v грамм (каждую гирю
    # можно взять либо 0, либо 1 раз).
    v = int(input())
    weights = [100, 200, 300, 500, 1000, 1200, 1400, 1500, 2000, 3000]
    count = 0
    # перебор всех подмножеств (2^10 = 1024 вариантов)
    for mask in range(1 << len(weights)):
        s = 0
        for i in range(len(weights)):
            if mask & (1 << i):
                s += weights[i]
        if s == v:
            count += 1
    print(count)


if __name__ == "__main__":
    main()
