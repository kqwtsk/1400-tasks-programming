def main():
    # Последовательность дробей: 1/1, 2/1, 3/2, ...
    # Числитель (знаменатель) следующего члена равен сумме
    # числителей (знаменателей) двух предыдущих.
    # Нужно найти первый член, который отличается от предыдущего
    # не более чем на 0.001.
    eps = 0.001
    # первые два члена: 1/1 и 2/1
    a1, b1 = 1.0, 1.0
    a2, b2 = 2.0, 1.0
    prev = a2 / b2
    cur = prev
    while True:
        # следующий член
        a3 = a1 + a2
        b3 = b1 + b2
        cur = a3 / b3
        if abs(cur - prev) <= eps:
            print(cur)
            break
        # сдвигаем окно
        a1, b1, a2, b2 = a2, b2, a3, b3
        prev = cur


if __name__ == "__main__":
    main()
