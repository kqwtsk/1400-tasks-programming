def term(n: int) -> float:
    return 1.0 + 1.0 / n


def main():
    # Рассмотрим последовательность 1+1/2, 1+1/3, ..., 1+1/n.
    # Напечатать все значения n, при которых все числа последовательности
    # не меньше a (1 < a <= 1.5).
    a = float(input())
    n = 2
    # Поскольку последовательность 1 + 1/n убывает по n,
    # условие "все >= a" эквивалентно "последний >= a".
    while True:
        t = term(n)
        if t >= a:
            print(n)
            n += 1
        else:
            break


if __name__ == "__main__":
    main()
